#define frame(i) ((((i))/8)%12)

#define rainbowcolor(i,j) (0x7F*((rainbow[i] >> (j))&3))

int16_t sparklesX[] = { 4, 33, 43,  63, 31, 2,
                        64+4, 64+33, 64+43,  64+63, 64+31, 64+2};
char sparklesY[] = {1,  10, 20, 41, 51, 61, 1,  10, 20, 41, 51, 61};
char sparklesF[] = {1, 3, 1, 5, 5, 2, 1, 3, 1, 5, 5, 2};

char catX = 47;
char catY = 20;

void loop()
{
  if (!(arduboy.nextFrame()))
    return;
  static uint16_t i = 0;
  arduboy.setRGBled(rainbowcolor(frame(i)/2,4),rainbowcolor(frame(i)/2,2),rainbowcolor(frame(i)/2,0));//2 0 0
  
  arduboy.clear();

 // move cat
 if (arduboy.pressed(LEFT_BUTTON))
    if (catX > 1)
      catX -= 1;
  if (arduboy.pressed(RIGHT_BUTTON))
    if (catX < 128-34)
      catX += 1;  

  if (arduboy.pressed(UP_BUTTON))
    if (catY > 1)
      catY -= 1;
  if (arduboy.pressed(DOWN_BUTTON))
    if (catY < 64-24)
      catY += 1;  

  // draw "rainbow"
  for(char k=catX-4;k+14 > -14; k -= 14)
    arduboy.drawBitmap(k,catY,rainbow_png_data + (14*3)*((frame(i)/4)%2) + (14*3*2*(i%2)),14,24,WHITE);
  
  // draw sparkles
  for(char j=0;j<12;j++)
  {
    sparklesX[j] -= 1;/*-8*frame(i)));*/
    if (sparklesX[j] < -8)
      sparklesX[j] += 128;
    arduboy.drawBitmap(sparklesX[j],sparklesY[j]-3,sparkle + 8*((
      (frame(i)/2) + sparklesF[j]
      
    )%6),8,8,WHITE);
  }

  // draw cat
  arduboy.drawBitmap(catX,catY,nyan_png_data + (34*3)*(frame(i)/2) + (34*3*6*(i%2)),34,24,WHITE);

  arduboy.display();
  
  i++;
  if (i == 192*10)
    i = 0;
}